CREATE TYPE "tipo_usuario" AS ENUM (
  'administrador',
  'bibliotecario',
  'profesor',
  'alumno'
);

CREATE TYPE "estado_usuario" AS ENUM (
  'activo',
  'suspendido',
  'bloqueado'
);

CREATE TYPE "disponible" AS ENUM (
  'disponible',
  'prestado',
  'reservado',
  'dañado',
  'perdido',
  'obsoleto'
);

CREATE TYPE "estado_prestamo" AS ENUM (
  'activo',
  'vencido',
  'devuelto',
  'perdido'
);

CREATE TYPE "tipo_prestamo" AS ENUM (
  'interno',
  'externo'
);

CREATE TYPE "accion_iso" AS ENUM (
  'crear',
  'actualizar',
  'borrar',
  'prestamo',
  'devolucion',
  'renovacion',
  'pago_multa'
);

CREATE TABLE "usuarios" (
  "id_usuario" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "nombre_completo" varchar(150) NOT NULL,
  "email" varchar(150) UNIQUE NOT NULL,
  "contrasena" varchar(255) NOT NULL,
  "tipo" tipo_usuario NOT NULL,
  "estado" estado_usuario DEFAULT 'activo',
  "matricula_empleado" varchar(20) UNIQUE,
  "creado_en" timestamp DEFAULT (now())
);

CREATE TABLE "editoriales" (
  "id_editorial" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "nombre" varchar(150) NOT NULL,
  "pais" varchar(100)
);

CREATE TABLE "categorias" (
  "id_categoria" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "nombre" varchar(100) NOT NULL
);

CREATE TABLE "libros" (
  "id_libro" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "titulo" varchar(255) NOT NULL,
  "autor" varchar(150),
  "isbn" varchar(20) UNIQUE,
  "ficha_bibliografica" text,
  "id_editorial" bigint,
  "id_categoria" bigint
);

CREATE TABLE "ejemplares" (
  "id_ejemplar" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "id_libro" bigint NOT NULL,
  "codigo_inventario" varchar(50) UNIQUE,
  "ubicacion_pasillo_estante" varchar(100),
  "disponible" disponible DEFAULT 'disponible',
  "precio_compra_usd" decimal(10,2),
  "fecha_adquisicion" date
);

CREATE TABLE "compras_libros" (
  "id_compra" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "proveedor" varchar(150),
  "factura" varchar(100),
  "fecha_compra" date,
  "monto_total" decimal(10,2)
);

CREATE TABLE "compras_detalle" (
  "id_detalle" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "id_compra" bigint,
  "id_ejemplar" bigint,
  "precio_unitario_usd" decimal(10,2)
);

CREATE TABLE "prestamos" (
  "id_prestamo" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "id_usuario" bigint NOT NULL,
  "id_ejemplar" bigint NOT NULL,
  "tipo" tipo_prestamo NOT NULL,
  "estado" estado_prestamo DEFAULT 'activo',
  "fecha_salida" timestamp DEFAULT (now()),
  "fecha_vencimiento" timestamp NOT NULL,
  "fecha_devolucion" timestamp,
  "renovaciones_conteo" int DEFAULT 0,
  "autorizado_por" bigint,
  "condicion_entrega" varchar(255),
  "condicion_retorno" varchar(255),
  "folio_recibo" varchar(50)
);

CREATE TABLE "multas" (
  "id_multa" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "id_prestamo" bigint NOT NULL,
  "dias_retraso" int,
  "monto_por_dia" decimal(10,2) DEFAULT 10,
  "monto_total" decimal(10,2),
  "tipo_mora" varchar(50),
  "estado_pago" boolean DEFAULT false,
  "comprobante_tesoreria" varchar(100),
  "creado_en" timestamp DEFAULT (now())
);

CREATE TABLE "bitacora_iso_9001" (
