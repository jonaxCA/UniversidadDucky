CREATE TABLE "integracion_escolar" (
  "id_escolar" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "id_usuario" bigint UNIQUE NOT NULL,
  "matricula_escolar" varchar(20) UNIQUE,
  "carrera" varchar(100),
  "situacion_academica" varchar(50),
  "periodo_activo" boolean DEFAULT true,
  "ultima_sincronizacion" timestamp DEFAULT (now())
);

CREATE TABLE "integracion_rh" (
  "id_rh" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
